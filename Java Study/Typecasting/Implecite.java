package Typecasting;

public class Implecite {
    public static void main(String[] args) {
        int a =10;
        double b = a;
        System.out.println(b);

        byte x = 127;
        int  y = x;
        System.out.println(y);

        byte z = 123;
        short s = z;
        System.out.println(s);

        int i = 1345622;
        long l = i;
        System.out.println(l);

        int d = 1234;
        float f = d;
        System.out.println(f);

        int m =48;
        System.out.println((char)m);
    }
}
